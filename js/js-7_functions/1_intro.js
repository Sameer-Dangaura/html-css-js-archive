//-> JavaScript Functions
//-> :
//->  A JavaScript function is a block of code designed to perform a particular task.

//-> A JavaScript function is executed when "something" invokes it (calls it).

//---------------------------------------------------
//-> Tip:
//-> In JavaScript, function parameters do not have a variable declaration keywords such as: var, let or const.
//---------------------------------------------------

function myFun(a, b) {
    return a * b;
}

//invoking function inside the console.log().
console.log( myFun(3, 5) );   //outputs: 15



