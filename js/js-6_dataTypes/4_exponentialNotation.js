//-> Exponential Notation
//-> :
//->  Extra large or extra small numbers can be written with scientific (exponential) notation:


let myValue1 = 123e5;   //=> 12300000
console.log(myValue1);  //outputs: 12300000

let myValue2 = 123e-5;   //=> 0.00123
console.log(myValue2);  //outputs: 0.00123


//-------
//-> Tip |
//-------
//-> :
//->  1e5 is a number expressed using scientific notation, specifically E notation, and it means 1 multiplied by 10 to the 5th power (the 'e' meaning 'exponent').

//-> So 1e5 equals 1*100000 and is equal to 100000. 