//-> Types of JavaScript Operators:

//-> 1) JavaScript Arithmetic operators 
//-> :
//->  Arithmetic Operators are used to perform arithmetic on numbers:


let num1 = 10;
let result = (100 + 50) * num1;

console.log(result);    //outputs: 1500



//--------------------------------------------------------
// Operator	    Description
// --------     ------------
// +	        Addition
// -	        Subtraction
// *	        Multiplication
// **	        Exponentiation (ES2016)
// /	        Division
// %	        Modulus (Division Remainder)
// ++	        Increment
// --	        Decrement


//-> Note
//-> :
//-> In above mentioned all operators are same as other programming languages(like C, C++) except exponent.
//--------------------------------------------------------



//-> Exponent operation:
let number1 = 4;
let squareValue = number1 ** 2; //i.e. 4^2 in general.
console.log(squareValue);   //outputs: 16
