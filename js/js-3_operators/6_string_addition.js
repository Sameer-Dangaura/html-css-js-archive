//-> JavaScript String Addition (String concatenation)
//-> :
//->  The + can also be used to add (concatenate) strings:
//--------------------------------------------------------
//-> You can visit "2_js_type_conversion/2_implicitConversionToString.js".
//--------------------------------------------------------



let firstName = "John";
let lastName = "Doe";

console.log(firstName + " " + lastName);    //outputs: "John Doe"




//-> The += assignment operator can also be used to add (concatenate) strings:

let friendName = "John ";
friendName += "Son";

console.log(friendName); //outputs: John Son