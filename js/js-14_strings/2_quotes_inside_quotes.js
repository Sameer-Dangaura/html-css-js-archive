//-> Quotes Inside Quotes
//-> :
//-> You can use quotes inside a string, as long as they don't match the quotes surrounding the string:

let text4 = "It's alright";
console.log(text4); //outputs: It's alright
console.log(typeof text4);  //outputs: string

let text5 = "He is called as 'Johnny'";
console.log(text5); //outputs: He is called as 'Johnny'
console.log(typeof text5);  //outputs: string

let text6 = 'He is called as "Johnny"';
console.log(text6); //outputs: He is called as "Johnny"
console.log(typeof text6);  //outputs: string

