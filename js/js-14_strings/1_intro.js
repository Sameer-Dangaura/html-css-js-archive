//-> JavaScript Strings
//-> :
//-> Strings are for storing text.

//-> Strings are written with quotes.



//-> Using Quotes
//-> :
//-> A JavaScript string is zero or more characters written inside quotes.

let text1 = ""; // String with zero characters inside quotes i.e. empty string
console.log(text1); //outputs: 
console.log(typeof text1, "\n");    //outputs: string


let text2 = "John Doe is my friend";   // String with more characters written inside quotes
console.log(text2); //outputs: John Doe is my friend
console.log(typeof text2);  //outputs: string



// You can use single or double quotes for string in JS:
let text3 = 'Jane Doe is my girl friend';
console.log(text3); //outputs: Jane Doe is my girl friend
console.log(typeof text3);  //outputs: string


/*--------------------------------------------------------
# Note
Strings created with single or double quotes work the same.

There is no difference between the two.
--------------------------------------------------------*/
