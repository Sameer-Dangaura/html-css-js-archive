//-> String Length
//-> :
//-> To find the length of a string, use the built-in       length      property:

let text1 = "Hello, I am John Doe";


//-> find the length:

let lengthOfText = text1.length;    //Here, length is in-built property to find length of a string.

console.log("length of text1:", lengthOfText);  //outputs: length of text1: 20
//-> or,
console.log("length of text1:", text1.length);  //outputs: length of text1: 20
