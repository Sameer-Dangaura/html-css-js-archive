//-> Displaying Object Properties by name
//-> :
//-> This method, we having using till now.
//-> The properties of an object can be displayed as a string:


//Create an Object:
const person = {
  name: "John",
  age: 30,
  city: "New York"
};

//Display object properties by their name:

console.log(person.name, "is", person.age, "years old.");
//outputs: John is 30 years old.

console.log(person.name, "lives in", person.city);
//outputs: John lives in New York
