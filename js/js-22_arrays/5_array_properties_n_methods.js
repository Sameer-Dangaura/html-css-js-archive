//-> Array Properties and Methods
//-> :
//-> The real strength of JavaScript arrays are the built-in array properties and methods:

//-> array_name.length
//-> : 
//-> The length property of an array returns the length of an array (the number of array elements). 

//-> array_name.sort()
//-> :
//-> The sort() method arrange the elements of an array in ascending order.

const fruitBox = ["Papaya", "Apple", "Banana", "Orange"];
console.log(fruitBox.length);      
//-> outputs: 4
console.log(fruitBox.sort());   
//-> outputs: [ 'Apple', 'Banana', 'Orange', 'Papaya' ]
