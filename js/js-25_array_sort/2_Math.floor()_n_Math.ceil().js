//-> JavaScript Math.floor()
//-> :
//-> The Math.floor() method rounds a number DOWN to the nearest integer.

console.log(Math.floor(1.9));   //outputs: 1
console.log(Math.floor(1.4));   //outputs: 1
console.log(Math.floor(1));   //outputs: 1
console.log(Math.floor(0.2));   //outputs: 0
console.log(Math.floor(-1.2));   //outputs: -2



//-> JavaScript Math.ceil()
//-> :
//-> The Math.ceil() method rounds a number rounded UP to the nearest integer.
console.log(Math.ceil(1.9));    //outputs: 2
console.log(Math.ceil(1.4));    //outputs: 2
