//-> Value =  undefined  :
//-> when you declare a variable without assigning a value to it,
//-> JavaScript automatically assigns it the value   undefined .

let Name;
console.log(Name);  //outputs: undefined


//-> Here, Name  is declared but not initialized, so its value is   undefined .

//-> If you attempt to use a variable that has been declared but not initialized, you will get   undefined   when trying to access its value.
//-> this helps in debugging and understanding the state of variables in your code.