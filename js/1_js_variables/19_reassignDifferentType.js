//-> Examples for variables:

//-> Example-4:

//-> Variables in JavaScript are not limited to holding values of a single type;
//-> they can be reassigned to hold different types of values over their lifecycle.

let data = 2;   //start as a number
data = "Tina";  //Now it's a string
data = ["Raju", 32, "mnr"];     //Now it's an array

console.log(data);  //outputs: [ 'Raju', 32, 'mnr' ]