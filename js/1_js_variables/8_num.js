//-> Variable types:
//-> 1) numbers:

//-> JavaScript allows you to store both whole numbers(integers) and decimal numbers(floats) in variable.

//-> There is no need to declare the type of the variable beforehand in JavaScript like C, C++ (e.g: int x; float y;)

let myNum = 30000000;       //an integer value stored.
let myValue = 399.337333;   //a float value stored.

console.log(myNum); //outputs: 30000000
console.log(myValue);   //outputs: 399.337333