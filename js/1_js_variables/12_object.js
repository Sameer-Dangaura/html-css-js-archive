//-> Variable types:
//-> 5) objects:

//-> Objects represent groups of associated data and functions, which are stored as key:value pairs.

//-> They are enclosed in curly braces({}), making it easy to store and access data properties using keys.

let person = {
    name: "Shyam Lal",
    age: 32,
    address: "mnr",
    phone: 123456789
};

console.log(person);       //outputs:{ name: 'Shyam Lal', age: 32, address: 'mnr', phone: 123456789 }

//-> you can reach/access to a specific properties of an object by employing dot(.) notation:
console.log(person.name);   //outputs: Shyam Lal