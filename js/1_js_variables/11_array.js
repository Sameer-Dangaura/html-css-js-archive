//-> 4) arrays:

//-> An array is the collection of values, which can be of any type, stored in a single variable.

//-> Array are enclosed in square brackets([]), and their values are separated by commas.

//-> Each item in an array has an index starting with zero for the first element.

let colorsName = ['red', 'green' , 'blue', 'yellow'];
let primeNumbers = [2, 3, 5, 7, 11];

console.log(colorsName[0]); //outputs: red
console.log(colorsName[2]); //outputs: blue
console.log(primeNumbers[1]);   //outputs: 3