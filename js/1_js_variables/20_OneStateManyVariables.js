//-> One statement, many variables:

//-> You can declare many variables in one statement,
//-> for that you can use   var,   let,  or   const   for such declarations, depending on the scope and reassignment needs of the variables being declared.

const Name = "Alison", age = 30, isStudent = false;


//-> But, declaring multiple variables in a single statement is generally not considered good practice as it can lead to decreased code readability and potential confusion about the purpose and type of each variable, especially when dealing with complex codebases; it's often better to declare each variable on its own line for clarity:

//-> Example:

const myName = "John Doe";
const myAge = "24";
const myAddress = "MNR";

//-> now it is in more readable format...