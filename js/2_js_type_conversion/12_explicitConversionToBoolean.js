//-> 2) Explicit conversion (Type casting):

//-> c) Convert to Boolean explicitly
//-> :
//->  This involves explicitly converting a value to boolean type using the   Boolean()    function.

//-> It ensures that the value is interpreted as either true or false, faciliating boolean operations and logic.

let value = 20;
let boolValue = Boolean(value);

console.log(value);
console.log(boolValue); //outputs: true

//--------------------------------------------------------
// +ve value = true
// -ve value = true
// -0 or 0 = false
//--------------------------------------------------------

